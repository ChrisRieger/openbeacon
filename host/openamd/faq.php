<li><a name="amd">What is the OpenAMD project?</a></li>
The Open Attendee Meta-Data Project (OpenAMD) takes "RFID", an emerging hardware technology, and shows how it can be used in a socially friendly way.  By combining RFID tracking with a social networking system that uses "interest tagging", we not only give people a better conference experience, but also allow them to connect with other users.
<Br>
<li><a name="rfid">What is RFID? How does it track me?</a></li>
RFID (radio frequency identification) is a technology that uses a chip to broadcast a detection point.  In this case we are using active RFID chips, which emit a radio pulse that is picked up by readers placed in the conference areas.  These chips will be found on your HOPE admission badge if you have preregistered.  Each user's pulse is recorded in a database and we can use it to follow people around the conference.  The tracking is only present in specified conference areas (users not on the Mezzanine level or the 18th floor of the hotel will not be tracked).
<br>
<li><a name="tracking">What, specifically, are you tracking?  If i walk into the conference, do you automatically have my name/address/credit card number/last blood test results?</a></li>
When a user is wearing the badge, the only things being tracked are badge number and location.  We do not have any other user information. If you choose to enter info on the site, all that info is being stored external to the badge, on our database.
<br>
<li><a name="interesttags">What are interest tags? How are they being used in OpenAMD?</a></li>
Interest tags are keywords used to tag users in much the same manner a site such as Flickr uses tags to identify and sort images.  Users can choose to login to the OpenAMD website and select up to five interests, which they can be sorted by.  These interests can be used to determine other people you may want to meet.
<br>
<li><a name="info">Why do you want all this other info? What are you doing with it?</a></li>
All personal information we collect is voluntary; however, the information we can collect will help enhance your convention experience.  Hometown location can be used for meeting others from your area.  Your cell number can provide you with text message reminders of talks you wish to catch, or of events you plan to attend.  Your interest info can not only allow you to streamline your choices of talks to what you are most interested in, but will help group you with others who share your interests during the games.
<br><br>
We absolutely are not going to release this information to any third party, or to anyone outside the project itself. All info you submit is confidential within the bounds of the OpenAMD project and the HOPE conference.  Anonymous location data will be released after the conference, but this data will not include any personal data.
<br>
<li><a name="important">Why is all this important?</a></li>
This project is designed to explore RFID technology, a tech that is usually considering intimidating and flavored with Big Brother-style abilities, in a transparent and accessible manner.  We want to both stretch the limits of how RFID can be applied, and make people aware of how it works and what it's like to work with it.  We are presenting this project to the public both in order to mitigate the fear response many people will have to using RFID widely, and to encourage people to embrace it and take the technology into their own hands.  If we do not, then who will?
